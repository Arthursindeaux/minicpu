#include <stdio.h>
#include <stdint.h>

uint8_t mem[256] = {0};
uint8_t reg[4] = {0};
uint8_t pc = 0, zf = 0, running = 1;
int ciclo = 0;

void fetch(uint8_t *op, uint8_t *a, uint8_t *b) {
    *op = mem[pc];
    *a = mem[pc + 1];
    *b = mem[pc + 2];
    pc += 3;
}

void decode_execute(uint8_t op, uint8_t a, uint8_t b) {
    switch (op) {
        case 0x01: 
            reg[a] = mem[b];
            break;

        case 0x02: 
            mem[b] = reg[a];
            break;

        case 0x03: 
            reg[a] = reg[a] + reg[b];
            break;

        case 0x04: 
            reg[a] = reg[a] - reg[b];
            break;

        case 0x05: 
            reg[a] = b;
            break;

        case 0x06:
            zf = (reg[a] == reg[b]) ? 1 : 0;
            break;

        case 0x07: 
            pc = a;
            break;

        case 0x08: 
            if (zf) pc = a;
            break;

        case 0x09: 
            if (!zf) pc = a;
            break;

        case 0x0A: 
            running = 0;
            break;

        default:
            printf("Opcode invalido: 0x%02X\n", op);
            running = 0;
            break;
    }
}

void trace(uint8_t op, uint8_t a, uint8_t b) {
    const char *nomes[] = {
        "", "LOAD", "STORE", "ADD", "SUB",
        "MOV", "CMP", "JMP", "JZ", "JNZ", "HALT"
    };

    const char *nome = (op <= 0x0A) ? nomes[op] : "???";

    printf("Ciclo %2d: %-5s %3d,%3d | R0=%3d R1=%3d R2=%3d R3=%3d | PC=%3d ZF=%d | MEM[0x20]=%3d\n",
           ciclo, nome, a, b,
           reg[0], reg[1], reg[2], reg[3],
           pc, zf, mem[0x20]);
}

